using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using GreenZoneParser.Lexer;

namespace GreenZoneParser.Parsers.Cs
{
    public class EnumDefStatementNode : TypeDefStatementNode
    {
        public EnumDefStatementNode(CsParser parser, BlockNode parent, Token startToken, Token endToken, List<Token> identifierTokens, bool includesSemicolon, List<AttributeNode> attributes, List<ModifierNode> modifiers, BlockNode subStatementsBlock, StatementNode tailStatement, StatementParseMode parseMode)
            : base(StatementId.EnumDef, parser, parent, startToken, endToken, identifierTokens, includesSemicolon, attributes, modifiers, subStatementsBlock, tailStatement, parseMode)
        {
            parser.RaiseNodeCreated(this);
        }
    }
}
